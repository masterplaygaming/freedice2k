// Function to detect browser language
function detectBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const shortLang = browserLang.split('-')[0];
    
    // Check if we support this language
    if (translations[shortLang]) {
        return shortLang;
    }
    
    // Default to English if not supported
    return 'en';
}

// Function to apply translations to the page
function applyTranslations(lang) {
    if (!translations[lang]) {
        console.error(`Language ${lang} not supported, falling back to English`);
        lang = 'en';
    }
    
    const t = translations[lang];
    
    // Update hero section
    document.querySelector('.hero-title').textContent = t.hero_title;
    document.querySelector('.hero-subtitle').textContent = t.hero_subtitle;
    document.querySelector('.btn-primary .button-text').textContent = t.start_button;
    
    // Update features
    const features = document.querySelectorAll('.feature span');
    if (features.length >= 3) {
        features[0].textContent = t.features.instant;
        features[1].textContent = t.features.safe;
        features[2].textContent = t.features.works;
    }
    
    // Update gem packs section
    document.querySelector('.gem-packs-section .section-title').textContent = t.packs_title;
    document.querySelector('.gem-packs-section .section-subtitle').textContent = t.packs_subtitle;
    
    // Update select buttons
    document.querySelectorAll('.select-button').forEach(button => {
        button.textContent = t.select_button;
    });
    
    // Update tags
    const popularTag = document.querySelector('.popular-tag');
    const valueTag = document.querySelector('.value-tag');
    if (popularTag) popularTag.textContent = t.popular_tag;
    if (valueTag) valueTag.textContent = t.best_value_tag;
    
    // Update testimonials section
    document.querySelector('.testimonials-section .section-title').textContent = t.testimonials_title;
    
    // Update how it works section
    document.querySelector('.how-it-works-section .section-title').textContent = t.how_it_works_title;
    
    // Update steps
    const steps = document.querySelectorAll('.step-content');
    if (steps.length >= 4) {
        steps[0].querySelector('h3').textContent = t.steps.step1_title;
        steps[0].querySelector('p').textContent = t.steps.step1_desc;
        steps[1].querySelector('h3').textContent = t.steps.step2_title;
        steps[1].querySelector('p').textContent = t.steps.step2_desc;
        steps[2].querySelector('h3').textContent = t.steps.step3_title;
        steps[2].querySelector('p').textContent = t.steps.step3_desc;
        steps[3].querySelector('h3').textContent = t.steps.step4_title;
        steps[3].querySelector('p').textContent = t.steps.step4_desc;
    }
    
    // Update status bar
    const statusLabels = document.querySelectorAll('.status-label');
    if (statusLabels.length >= 2) {
        statusLabels[0].textContent = t.status.android;
        statusLabels[1].textContent = t.status.ios;
    }
    
    const statusIndicators = document.querySelectorAll('.status-indicator span');
    statusIndicators.forEach(indicator => {
        if (indicator.parentElement.classList.contains('online')) {
            indicator.textContent = t.status.online;
        } else {
            indicator.textContent = t.status.maintenance;
        }
    });
    
    // Update counters
    const counterLabels = document.querySelectorAll('.counter-label');
    if (counterLabels.length >= 3) {
        counterLabels[0].textContent = t.status.visitors;
        counterLabels[1].textContent = t.status.gems_generated;
        counterLabels[2].textContent = t.status.tasks_today;
    }
    
    // Update activity feed
    const activityHeader = document.querySelector('.activity-header span');
    if (activityHeader) activityHeader.textContent = t.activity.title;
    
    // Update platform modal
    const platformModal = document.getElementById('platform-modal');
    if (platformModal) {
        platformModal.querySelector('.modal-header h3').textContent = t.platform_modal.title;
        platformModal.querySelector('.modal-description').textContent = t.platform_modal.desc;
        
        const platformOptions = platformModal.querySelectorAll('.platform-option span');
        if (platformOptions.length >= 2) {
            platformOptions[0].textContent = t.platform_modal.android;
            platformOptions[1].textContent = t.platform_modal.ios;
        }
    }
    
    // Update player ID modal
    const playerIdModal = document.getElementById('playerid-modal');
    if (playerIdModal) {
        playerIdModal.querySelector('.modal-header h3').textContent = t.playerid_modal.title;
        playerIdModal.querySelector('.modal-description').textContent = t.playerid_modal.desc;
        playerIdModal.querySelector('label').textContent = t.playerid_modal.label;
        playerIdModal.querySelector('input').placeholder = t.playerid_modal.placeholder;
        playerIdModal.querySelector('.form-help').textContent = t.playerid_modal.help;
        playerIdModal.querySelector('.continue-button .button-text').textContent = t.playerid_modal.continue;
    }
    
    // Update generator modal
    const generatorModal = document.getElementById('generator-modal');
    if (generatorModal) {
        generatorModal.querySelector('.modal-header h3').textContent = t.generator_modal.title;
        
        const infoLabels = generatorModal.querySelectorAll('.info-label');
        if (infoLabels.length >= 3) {
            infoLabels[0].textContent = t.generator_modal.info.playerid;
            infoLabels[1].textContent = t.generator_modal.info.platform;
            infoLabels[2].textContent = t.generator_modal.info.gems;
        }
        
        generatorModal.querySelector('.console-header span').textContent = t.generator_modal.console;
        
        const progressLabel = generatorModal.querySelector('.progress-label span:first-child');
        if (progressLabel) progressLabel.textContent = t.generator_modal.progress;
    }
    
    // Update verification modal
    const verificationModal = document.getElementById('verification-modal');
    if (verificationModal) {
        verificationModal.querySelector('.modal-header h3').textContent = t.verification_modal.title;
        verificationModal.querySelector('.verification-message h4').textContent = t.verification_modal.heading;
        
        const verificationTexts = verificationModal.querySelectorAll('.verification-message p');
        if (verificationTexts.length >= 2) {
            verificationTexts[0].textContent = t.verification_modal.desc1;
            
            // Handle special case with gem amount
            const gemAmount = document.getElementById('verification-gems').textContent;
            verificationTexts[1].innerHTML = t.verification_modal.desc2.replace('{gems}', `<span id="verification-gems">${gemAmount}</span>`);
        }
        
        const verificationStatuses = verificationModal.querySelectorAll('.verification-info .info-item span');
        if (verificationStatuses.length >= 3) {
            verificationStatuses[0].textContent = t.verification_modal.status.generated;
            verificationStatuses[1].textContent = t.verification_modal.status.found;
            verificationStatuses[2].textContent = t.verification_modal.status.waiting;
        }
        
        const verifyButton = verificationModal.querySelector('.verify-button .button-text');
        if (verifyButton) verifyButton.textContent = t.verification_modal.button;
    }
    
    // Update footer
    const footerTexts = document.querySelectorAll('.footer-content p');
    if (footerTexts.length >= 2) {
        footerTexts[0].textContent = t.footer.copyright;
        footerTexts[1].textContent = t.footer.disclaimer;
    }
    
    // Update language selector display
    const currentLanguage = document.querySelector('.current-language span');
    if (currentLanguage) {
        const langOption = document.querySelector(`.language-option[data-lang="${lang}"]`);
        if (langOption) {
            currentLanguage.textContent = langOption.textContent;
        }
    }
    
    console.log(`Language changed to: ${lang}`);
}

// Custom console message generator for different languages
function getConsoleMessages(lang, playerId, platform, gemAmount) {
    if (!translations[lang] || !translations[lang].generator_modal.console_messages) {
        lang = 'en';
    }
    
    const messages = translations[lang].generator_modal.console_messages;
    
    return [
        { text: messages.connecting, type: 'normal', delay: 500 },
        { text: messages.connected, type: 'success', delay: 800 },
        { text: messages.searching.replace('{playerId}', playerId), type: 'normal', delay: 1000 },
        { text: messages.found, type: 'success', delay: 1200 },
        { text: messages.platform_detected.replace('{platform}', platform === 'android' ? 'Android' : 'iOS'), type: 'normal', delay: 800 },
        { text: messages.preparing, type: 'normal', delay: 1000 },
        { text: messages.generating.replace('{gems}', gemAmount), type: 'normal', delay: 1500 },
        { text: messages.injecting, type: 'normal', delay: 1500 },
        { text: messages.complete, type: 'success', delay: 1000 },
        { text: messages.verification, type: 'warning', delay: 1000 }
    ];
}

// Initialize language detection and application on page load
document.addEventListener('DOMContentLoaded', function() {
    // Detect browser language
    const detectedLang = detectBrowserLanguage();
    
    // Apply translations
    applyTranslations(detectedLang);
    
    // Override generator animation to use translated messages
    window.startGenerator = function(gemAmount, playerId, platform) {
        const consoleOutput = document.getElementById('console-output');
        const progressFill = document.getElementById('progress-fill');
        const progressPercentage = document.getElementById('progress-percentage');
        
        // Clear previous console output
        consoleOutput.innerHTML = '';
        
        // Reset progress
        progressFill.style.width = '0%';
        progressPercentage.textContent = '0%';
        
        // Get translated console messages
        const currentLang = document.querySelector('.current-language span').textContent;
        const langCode = Array.from(document.querySelectorAll('.language-option'))
            .find(option => option.textContent === currentLang)?.getAttribute('data-lang') || 'en';
        
        const consoleMessages = getConsoleMessages(langCode, playerId, platform, gemAmount);
        
        // Add console messages with delays
        let totalDelay = 0;
        consoleMessages.forEach((message, index) => {
            totalDelay += message.delay;
            
            setTimeout(() => {
                // Add message to console
                const line = document.createElement('div');
                line.className = `console-line ${message.type}`;
                line.textContent = message.text;
                consoleOutput.appendChild(line);
                
                // Auto-scroll to bottom
                consoleOutput.scrollTop = consoleOutput.scrollHeight;
                
                // Update progress based on message index
                const progress = Math.min(100, Math.round((index + 1) / consoleMessages.length * 100));
                progressFill.style.width = `${progress}%`;
                progressPercentage.textContent = `${progress}%`;
                
                // If last message, show verification modal after a delay
                if (index === consoleMessages.length - 1) {
                    setTimeout(() => {
                        closeModal('generator-modal');
                        openModal('verification-modal');
                    }, 1200);
                }
            }, totalDelay);
        });
    };
});
